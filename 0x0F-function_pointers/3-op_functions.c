#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - func add
 * @a: int
 * @b: int
 * Return: int
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - function subtracts
 * @a: int
 * @b: int
 * Return: int
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - function multiplies
 * @a: int
 * @b: int
 * Return: int
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - function div
 * @a: int
 * @b: int
 * Return: int
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - func modulo
 * @a: int
 * @b: int
 * Return: int
 */
int op_mod(int a, int b)
{
return (a % b);
}
